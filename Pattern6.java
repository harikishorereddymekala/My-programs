class Pattern6 
{
       public static void main(String[] args) 
         {
                int star = 5;
                for(int i = 5-1; i >= 0; i--){
                   star--;
                   for(int j = 0; j <= star; j++){
                      System.out.print("* ");
                   }
                    System.out.println(" ");
                   }
          }
}
       
            